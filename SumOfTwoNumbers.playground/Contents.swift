import UIKit

/*
 
 Create a function that takes two numbers as arguments and return their sum.
 
 Examples:
 
 addition(3, 2) ➞ 5

 addition(-3, -6) ➞ -9

 addition(7, 3) ➞ 10

*/

func addition(_ num1: Int, _ num2: Int) -> Int {
    return num1 + num2
}

addition(3, 2)
addition(-3, -6)
addition(7, 3)
